public class commands {
    public void add(int x, int y){
        int z = x + y;
        System.out.println(z);
    }
    public void sub(int x, int y){
        int z = x - y;
        System.out.println(z);
    }
    public void mul(int x, int y){
        int z = x * y;
        System.out.println(z);
    }
    public void div(int x, int y){
        int z = x / y;
        System.out.println(z);
    }
    public void remainder(int x, int y){
        int z = x % y;
        System.out.println(z);
    }


}
