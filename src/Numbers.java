public class Numbers {
    private int x;
    private int y;
    public Numbers(int x, int y){
        this.x = x;
        this.y = y;
    }
    public Integer getX(){
        return x;
    }
    public Integer getY(){
        return y;
    }
    public void setX(int z){
        x = z;
    }
    public void setY(int z){
        y = z;
    }
}
